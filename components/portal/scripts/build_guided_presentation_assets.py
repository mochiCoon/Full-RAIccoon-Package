def main():
    missing = [str(ARTIFACTS / seg.screenshot) for seg in SEGMENTS if not (ARTIFACTS / seg.screenshot).exists()]
    if missing:
        raise SystemExit("Missing screenshots:\n" + "\n".join(missing))
    if not AUDIO.exists():
        raise SystemExit(f"Missing narration audio: {AUDIO}")
    audio_duration = ffprobe_duration(AUDIO)
    frames = build_frames_and_srt(audio_duration)
    build_video(frames, audio_duration)
    build_pptx()
    pdf_exported = export_pdf()
    video_duration = ffprobe_duration(VIDEO)
    write_readme(audio_duration, video_duration)
    print(json.dumps({
        "pptx": str(PPTX),
        "pdf": str(PDF) if pdf_exported else None,
        "video": str(VIDEO),
        "audio": str(AUDIO),
        "captions": str(SRT),
        "readme": str(README),
        "audio_duration": round(audio_duration, 3),
        "video_duration": round(video_duration, 3),
        "frames": len(frames),
    }, indent=2))


if __name__ == "__main__":
    main()
